bool promising(int i,int vcolor[N+1],int W[N+1][N+1]) {
	int j;
	bool s;
	s = true;
	j = 1;

	while (j < i && s) {
		if (W[i][j] && vcolor[i] == vcolor[j])
			s = false;
		j++;
	}
	return s;
}